/* ======================== 마이크로 렉서 ======================== */

int64_t 렉스_숫자_개수(int64_t 소스) {
    int64_t 카운트 = 0;

    if (소스 > 0) {
        카운트 = 1;
    }

    return 카운트;
}

/* ======================== 마이크로 파서 ======================== */

int64_t 파스_숫자(int64_t 토큰) {
    return 토큰;
}

int64_t 파스_표현식(int64_t 토큰1, int64_t 토큰2) {
    return 토큰1 + 토큰2;
}

/* ======================== 마이크로 코드 생성기 ======================== */

int64_t 코드젠_헤더() {
    return 1;
}

int64_t 코드젠_함수(int64_t 이름, int64_t 본체) {
    return 본체;
}

int64_t 코드젠_메인(int64_t 식) {
    return 식;
}

/* ======================== 통합 컴파일러 ======================== */

int64_t 컴파일_마이크로(int64_t 소스) {
    /* Step 1: 렉싱 */
    int64_t 토큰 = 렉스_숫자_개수(소스);

    /* Step 2: 파싱 */
    int64_t ast = 파스_숫자(토큰);

    /* Step 3: 코드 생성 */
    int64_t 헤더 = 코드젠_헤더();
    int64_t 코드 = 코드젠_메인(ast);

    /* Step 4: 반환 */
    return 코드;
}

