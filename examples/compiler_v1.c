/* ======================== 마이크로 렉서 ======================== */

bool 문자_숫자인가(int64_t c) {
    return c >= 48 && c <= 57;
}

bool 문자_문자인가(int64_t c) {
    bool 소문자 = c >= 97 && c <= 122;
    bool 대문자 = c >= 65 && c <= 90;
    return 소문자 || 대문자;
}

bool 문자_공백인가(int64_t c) {
    return c == 32 || c == 9;
}

int64_t 렉서_토큰_계산(int64_t 길이) {
    int64_t 토큰 = 0;

    if (길이 > 0) {
        토큰 = 1;
    }

    if (길이 > 5) {
        토큰 = 토큰 + 1;
    }

    if (길이 > 10) {
        토큰 = 토큰 + 1;
    }

    return 토큰;
}

/* ======================== 마이크로 파서 ======================== */

int64_t 파서_숫자_노드(int64_t 값) {
    return 값;
}

int64_t 파서_이항연산(int64_t 좌, int64_t 우) {
    return 좌 + 우;
}

int64_t 파서_함수호출(int64_t 함수, int64_t 인자) {
    return 함수 + 인자;
}

/* ======================== 마이크로 코드생성기 ======================== */

int64_t 코드젠_숫자(int64_t 값) {
    int64_t 코드 = 값;
    return 코드;
}

int64_t 코드젠_표현식(int64_t 좌, int64_t 우) {
    return 좌 + 우;
}

int64_t 코드젠_함수_시작() {
    return 1;
}

int64_t 코드젠_함수_끝() {
    return 1;
}

/* ======================== 통합 컴파일러 ======================== */

int64_t 컴파일_렉스(int64_t 소스) {
    return 렉서_토큰_계산(소스);
}

int64_t 컴파일_파스(int64_t 토큰) {
    return 파서_숫자_노드(토큰);
}

int64_t 컴파일_코드젠(int64_t ast) {
    return 코드젠_숫자(ast);
}

int64_t 컴파일_통합(int64_t 소스) {
    /* Step 1: 렉싱 */
    int64_t 토큰 = 컴파일_렉스(소스);

    /* Step 2: 파싱 */
    int64_t ast = 컴파일_파스(토큰);

    /* Step 3: 코드생성 */
    int64_t 코드 = 컴파일_코드젠(ast);

    /* Step 4: 반환 */
    return 코드;
}

