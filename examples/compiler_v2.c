#include <stdio.h>
#include <stdbool.h>
#include <stdint.h>

/* Phase 5: 마이크로 컴파일러 v2 - 확장 버전 */

#define TOKEN_NUMBER 1
#define TOKEN_IDENT 2
#define TOKEN_PLUS 3
#define TOKEN_ASSIGN 4
#define TOKEN_EOF 5

/* ======================== 렉서 ======================== */

int64_t 렉서_고급(int64_t 길이, int64_t 복잡도) {
    int64_t 토큰_카운트 = 0;

    if (길이 > 0) {
        토큰_카운트 = 1;
    }

    if (복잡도 > 0) {
        토큰_카운트 = 토큰_카운트 + 1;
    }

    if (복잡도 > 1) {
        토큰_카운트 = 토큰_카운트 + 2;
    }

    if (복잡도 > 2) {
        토큰_카운트 = 토큰_카운트 + 3;
    }

    return 토큰_카운트;
}

/* ======================== 파서 ======================== */

int64_t 파서_함수선언(int64_t 이름, int64_t 매개변수) {
    return 이름 + 매개변수;
}

int64_t 파서_변수선언(int64_t 이름, int64_t 타입) {
    return 이름 + 타입;
}

int64_t 파서_표현식_파싱(int64_t 좌, int64_t 우, int64_t 연산자) {
    if (연산자 == TOKEN_PLUS) {
        return 좌 + 우;
    }
    return 좌;
}

/* ======================== 코드생성 ======================== */

int64_t 코드젠_헤더() {
    return 1;
}

int64_t 코드젠_함수(int64_t 함수명, int64_t 바디) {
    int64_t 코드 = 함수명 + 바디;
    return 코드;
}

int64_t 코드젠_변수(int64_t 변수명, int64_t 타입, int64_t 값) {
    int64_t 코드 = 변수명 + 타입 + 값;
    return 코드;
}

int64_t 코드젠_메인(int64_t 본체) {
    return 본체;
}

/* ======================== 최적화 ======================== */

int64_t 최적화_상수전파(int64_t 값) {
    return 값;
}

int64_t 최적화_데드코드제거(int64_t 코드) {
    if (코드 > 0) {
        return 코드;
    }
    return 0;
}

/* ======================== 통합 v2 ======================== */

int64_t 컴파일_v2(int64_t 소스, int64_t 복잡도) {
    int64_t 토큰 = 렉서_고급(소스, 복잡도);
    int64_t 함수 = 파서_함수선언(토큰, 복잡도);
    int64_t 변수 = 파서_변수선언(1, 2);
    
    int64_t 헤더 = 코드젠_헤더();
    int64_t 함수_코드 = 코드젠_함수(함수, 변수);
    int64_t 메인 = 코드젠_메인(함수_코드);
    
    int64_t 최적화 = 최적화_상수전파(메인);
    int64_t 최종 = 최적화_데드코드제거(최적화);
    
    return 최종;
}

